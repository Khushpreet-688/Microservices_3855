import React, { useEffect, useState } from 'react'
import '../App.css';

export default function AppStats() {
    const [isLoaded, setIsLoaded] = useState(false);
    const [stats, setStats] = useState({});
    const [error, setError] = useState(null)

	const getStats = () => {
	
        fetch(`http://kafka-acit3855.canadacentral.cloudapp.azure.com/processing/reporting/stats`)
            .then(res => res.json())
            .then((result)=>{
				console.log("Received Stats")
                setStats(result);
                setIsLoaded(true);
            },(error) =>{
                setError(error)
                setIsLoaded(true);
            })
    }
    useEffect(() => {
		const interval = setInterval(() => getStats(), 2000); // Update every 2 seconds
		return() => clearInterval(interval);
    }, [getStats]);

    if (error){
        return (<div className={"error"}>Error found when fetching from API</div>)
    } else if (isLoaded === false){
        return(<div>Loading...</div>)
    } else if (isLoaded === true){
        return(
            <div>
                <h1>Latest Stats</h1>
                <table className={"StatsTable"}>
					<tbody>
						<tr>
							<th>Clock-in</th>
							<th>Clock-out</th>
						</tr>
						<tr>
							<td># Clock-ins: {stats['num_clock_ins']}</td>
							<td># Clock-outs: {stats['num_clock_outs']}</td>
						</tr>
						<tr>
							<td colspan="2">Average number of hours worked: {stats['avg_num_hours_worked']}</td>
						</tr>
						<tr>
							<td colspan="2">Max late arrival: {stats['max_late_arrival']}</td>
						</tr>
					</tbody>
                </table>
                <h3>Last Updated: {stats['last_updated']}</h3>

            </div>
        )
    }
}
